/*
 *      一、前端模块化的概念
 *          早期的JS代码使用有两种方式,一种是把所有的JS代码写在一个文件中,或者写在几个JS文件中然后引入使用。这样存在一系列的问题，例如耦合度太高、
 *       作用域污染、命名冲突、复用性低、维护性低等。前端模块化就是将一个复杂的程序依据一定的规则(规范)封装成几个块(文件), 并进行组合在一起
 * 　　　 块的内部数据与实现是私有的, 只是向外部暴露一些接口(方法)与外部其它模块通信。
 *
 *      二、常见的前端模块化规范
 *          -CommonJS   Node.js开发采用此规范
 *          -AMD
 *          -CMD
 *          -UMD
 *          -ES6
 *
 *
 */